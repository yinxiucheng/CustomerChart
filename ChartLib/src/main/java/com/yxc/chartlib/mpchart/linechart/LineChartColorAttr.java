package com.yxc.chartlib.mpchart.linechart;

/**
 * @author yxc
 * @since  2019-10-31
 */
public class LineChartColorAttr {
    public int lineColorResource;
    public int drawableResource;
    public int fillColorResource;

    public LineChartColorAttr(int lineColorResource, int drawableResource, int fillColorResource) {
        this.lineColorResource = lineColorResource;
        this.drawableResource = drawableResource;
        this.fillColorResource = fillColorResource;
    }
}
